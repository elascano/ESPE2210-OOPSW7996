    private void btnAccessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccessActionPerformed
        int option = cmbProducts.getSelectedIndex();
        if (option == 0) {
            JOptionPane.showMessageDialog(this, "No ha seleccionado ninguna opcion.\n Porfavor seleccione un producto.", "Advertencia en la selecci√≥n de datos", JOptionPane.WARNING_MESSAGE);
        } else if (option == 1) {
            FrmBeverage open = new FrmBeverage();
            open.setVisible(true);
            this.dispose();
        } else if (option == 2) {
            FrmCandy open = new FrmCandy();
            open.setVisible(true);
            this.dispose();
        } else if (option == 3) {
            FrmDairy open = new FrmDairy();
            open.setVisible(true);
            this.dispose();
        } else if (option == 4) {
            FrmGrain open = new FrmGrain();
            open.setVisible(true);
            this.dispose();
        } else if (option == 5) {
            FrmGrooming open = new FrmGrooming();
            open.setVisible(true);
            this.dispose();
        } else if (option == 6) {
            FrmMeat open = new FrmMeat();
            open.setVisible(true);
            this.dispose();
        } else if (option == 7) {
            FrmSnack open = new FrmSnack();
            open.setVisible(true);
            this.dispose();
        } else if (option == 8) {
            FrmVegetable open = new FrmVegetable();
            open.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnAccessActionPerformed

